    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed
        
        String isbr = jTextFieldISBR.getText();
        String titulo = jTextFieldTitulo.getText();

        Autor autor = null;
        Editora editora = null;
        AutorDAO daoAutor = new AutorDAO();
        EditoraDAO daoEditora = new EditoraDAO();
        
        for(Autor autorPonteiro: daoAutor.read()){
            if(String.valueOf(jComboBoxAutor.getSelectedItem()).equals(autorPonteiro.getNome()+" "+autorPonteiro.getSobrenome())){
                int idAutor = autorPonteiro.getId();
                autor = new Autor(idAutor);   
            }
        }
        
        for(Editora editoraPonteiro: daoEditora.read()){
            if(String.valueOf(jComboBoxEditora.getSelectedItem()).equals(editoraPonteiro.getNome())){
                int idEditora = editoraPonteiro.getId();
                editora = new Editora(idEditora);
            }
        }
      
        int Quantidade = Integer.parseInt(jTextFieldQuantidade.getText());
         
        if(editora!= null && autor != null){
            Livro livroNovo = new Livro(titulo, isbr, Quantidade, autor, editora);
            LivroDAO dao = new LivroDAO();
            dao.create(livroNovo);
        }
        this.dispose();
        
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

